def print_startup_banner(log_format: LogFormat, *, app_version: str) -> None:
    """Print the crown to stdout when logs are intended for humans."""

    if log_format == "json":
        return

    content = Group(
        Text(CROWN, style="lewisham.crown"),
        Text(),
        Align.center(Text("Lewisham Council Scraper", style="lewisham.title")),
        Align.center(Text(f"API Server · v{app_version}", style="lewisham.subtitle")),
    )
    panel = Panel.fit(
        content,
        style="lewisham.panel",
        border_style="lewisham.border",
        padding=(1, 1),
    )
    Console(width=PANEL_WIDTH, theme=LEWISHAM_CONSOLE_THEME).print(panel)
